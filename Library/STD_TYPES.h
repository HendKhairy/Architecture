	typedef unsigned long int u32;
	typedef unsigned long long int u64;

	typedef signed char s8;
	typedef signed short int s16;
	typedef signed long int s32;
	typedef signed long long int s64;

	typedef float  f32;
	typedef double f64;

	//pointers
	typedef u8* pu8;
	typedef u32* pu32;
	typedef u16* pu16;
	typedef u64* pu64;

	typedef s8* ps8;
	typedef s32* ps32;
	typedef s16* ps16;
	typedef s64* ps64;

	typedef f32* pf32;
	typedef f64* p64;
#endif